    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String cliente = txtCliente.getText();
        String cantidad = txtCantidad.getText();
        String producto = txtProducto.getText();
        Integer precio=Integer.valueOf(txtPrecio.getText()) ;
        String total=Integer. toString(precio*Integer.valueOf(cantidad));
        ////////////////////////
        try {   
            NuevaVenta cat = new NuevaVenta(null, cliente, producto, Integer.valueOf(cantidad),Integer. toString(precio), total);
            dao.nuevaventaIns(cat);
            Limpiar();
            CargarTabla1();
        } catch (Exception e) {
            dao.getMensaje();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String cliente = txtCliente.getText();
        String cantidad = txtCantidad.getText();
        String producto = txtProducto.getText();
        Integer precio=Integer.valueOf(txtPrecio.getText()) ;
        String total=Integer. toString(precio*Integer.valueOf(cantidad));

        ////////////////////////
        try {
            int fila = jTable1.getSelectedRow();
            int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
            NuevaVenta cat = new NuevaVenta(id, cliente, producto, Integer.valueOf(cantidad),Integer. toString(precio), total);
            dao.nuevaventaUpd(cat);
            Limpiar();
            CargarTabla1();
        } catch (NumberFormatException e) {
            dao.getMensaje();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

