    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      int respuesta=JOptionPane.showConfirmDialog(null,"Desa Eliminar","Alerta",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(respuesta==JOptionPane.YES_OPTION){
        int fila = jTable1.getSelectedRow();
        int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
        dao.nuevaventaDel(id);
        CargarTabla1();
        Limpiar();}
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int fila = jTable1.getSelectedRow();
            int id = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
            ///////////////
            try {
                NuevaVenta cat = dao.nuevaventaGet(id);
                txtCliente.setText(cat.getCliente());
                txtCantidad.setText(Integer. toString(cat.getCantidad()));
                txtPrecio.setText(cat.getPrecio());
                txtProducto.setText(cat.getProducto());
            } catch (Exception e) {
                dao.getMensaje();
            }

        } catch (NumberFormatException e) {
            dao.getMensaje();
        }
    }//GEN-LAST:event_jTable1MouseClicked

