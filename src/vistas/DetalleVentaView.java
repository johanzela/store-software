    private void CargarTabla1() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        double totalPagar=0;
        List<entidad.NuevaVenta> lista = dao.nuevaventaSel();
        ventas = new ArrayList<>();

        lista.forEach(t -> {
            ventas.add(new NuevaVenta(t.getId(), t.getCliente(), t.getProducto(), t.getCantidad(), t.getPrecio(), t.getTotal()));
        });
        for (NuevaVenta producto : ventas) {
            Object[] fila = {
                producto.getId(),
                producto.getCliente(),
                producto.getProducto(),
                producto.getCantidad(),
                producto.getPrecio(),
                producto.getTotal()};
            totalPagar=(double)(totalPagar+Integer. parseInt(producto.getTotal()));
            textTotalPagar.setText(Double.toString(totalPagar));
            tableModel.addRow(fila);
        }

    }
