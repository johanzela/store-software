    private void CargarTabla() {
        DefaultTableModel tableModel = (DefaultTableModel) tblEmployee.getModel();
        tableModel.setRowCount(0);
        List<entidad.Vendedor1> lista = dao.vendedorSel();
        vendedores = new ArrayList<>();
        /////////////
        List<entidad.NuevaVenta> lista1 = dao1.nuevaventaSel();
        ventas = new ArrayList<>();
        ///////////
        List<entidad.Cliente> lista2 = dao2.clienteSel();
        clientes = new ArrayList<>();
        ////////
        boletas = new ArrayList<>();
        //////////
        int a = lista1.size();
        for (int i = 0; i < a; i++) {
            boletas.add(new Ventas(lista1.get(i).getId(), lista2.get(i).getNombre(), lista.get(i).getNombre(), lista1.get(i).getTotal()));
        }
        /////
        for (Ventas boleta : boletas) {
            Object[] fila = {
                boleta.getId(),
                boleta.getCliente(),
                boleta.getVendedor(),
                boleta.getTotal()};
            tableModel.addRow(fila);

        }

    }

