    private void CargarTabla() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        List<entidad.Vendedor1> lista = dao.vendedorSel();
        vendedores=new ArrayList<>();
        
        lista.forEach(t -> {
            vendedores.add(new Vendedor1(t.getCodigo(), t.getDni(), t.getNombre(), t.getApellido()));
        });
        for (Vendedor1 vendedor : vendedores) {
            Object[] fila = {
                vendedor.getCodigo(),
                vendedor.getDni(),
                vendedor.getNombre(),
                vendedor.getApellido()};
            tableModel.addRow(fila);
        }

    }

